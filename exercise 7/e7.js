
// Arrays Methods

let colors = ["red", "blue", "green", "yellow"];
//before push
colors.push("purple");
console.log(colors);

// pop after push

colors.pop(); 
console.log(colors);



//unshift before pop
colors.unshift("orange");
console.log(colors);

//shift after unshift
colors.shift(); 
console.log(colors);


//length
console.log(colors.length);